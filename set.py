# set (집합)

a = set()
type (a)


a= {1,2,3}

# set은 중복을 허용하지 않는다.
a= set([0,0,0,1,2,3])
a
# >>> {0,1,2,3}

# & 교집합, | 합집합, - 차집합
a = set([1,2,3,4])
b = set([3,4,5,6])


a & b 
# >>> {3,4}

a | b
# >>> {1,2,3,4,5,6}

a - b 
# >>> {1, 2}


#요소추가 
a = {1,2,3,4,6}
a.add(7)
# >>> {1,2,3,4,6,7}
a.remove(3)
# >>> {1,2,4,6,7}
a.update([5,6,7,8])
# >>> {1,2,4,5,6,7,8} -> 중복허용X

